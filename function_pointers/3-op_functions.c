#include "3-calc.h"

/**
 * op_add - addition
 * @a: number 1
 * @b:number 2
 *
 * Return: value
 */

int op_add(int a, int b) { return a + b; }

/**
 * op_sub - subtraction
 * @a: number 1
 * @b:number 2
 *
 * Return: value
 */

int op_sub(int a, int b) { return a - b; }

/**
 * op_mul - multiplication
 * @a: number 1
 * @b:number 2
 *
 * Return: value
 */
int op_mul(int a, int b) { return a * b; }

/**
 * op_div - division
 * @a: number 1
 * @b:number 2
 *
 * Return: value
 */
int op_div(int a, int b) { return a / b; }

/**
 * op_mod - modular
 * @a: number 1
 * @b:number 2
 *
 * Return: value
 */
int op_mod(int a, int b) { return a % b; }
